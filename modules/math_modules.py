"""
We are going to use most of the functions available in the math module
"""

from math import *

# Factorial:
print('Factorial of 5: ',factorial(5))

# Square-root
print('Square-root of 25: ',sqrt(25))

# absolute
print('Factorials absolute of 10.5: ',fabs(-10.5))
print('Factorials absolute of 10.5:', fabs(10.5))

#  Ceiling
print('Ceiling of 10.2:',ceil(10.2))

# floor
print('Floor of 10.2: ',floor(10.2))

# Pi and pow
print('Area of circle with radius 5 is: ',(pi * pow(5,2)))


